async function seedPlayers() {
  console.log(`\n=== IPL 2026 Player Seed Script ===`);
  console.log(`Total players to upsert: ${players.length}`);
  console.log(`Supabase URL: ${supabaseUrl}\n`);

  const teamCounts: Record<string, number> = {};
  for (const pl of players) {
    teamCounts[pl.team] = (teamCounts[pl.team] || 0) + 1;
  }
  console.log('Players per team:');
  for (const [team, count] of Object.entries(teamCounts)) {
    console.log(`  ${team}: ${count}`);
  }
  console.log('');

  const BATCH_SIZE = 25;
  let inserted = 0;
  let errors = 0;

  for (let i = 0; i < players.length; i += BATCH_SIZE) {
    const batch = players.slice(i, i + BATCH_SIZE);
    const batchNum = Math.floor(i / BATCH_SIZE) + 1;
    const totalBatches = Math.ceil(players.length / BATCH_SIZE);

    process.stdout.write(`Upserting batch ${batchNum}/${totalBatches} (${batch.length} players)... `);

    const { error } = await supabase
      .from('players')
      .upsert(batch, { onConflict: 'name,team,season', ignoreDuplicates: false });

    if (error) {
      console.error(`FAILED`);
      console.error(`  Error: ${error.message}`);
      errors += batch.length;
    } else {
      console.log(`OK`);
      inserted += batch.length;
    }
  }

  console.log(`\n=== Seed Complete ===`);
  console.log(`  Upserted: ${inserted}`);
  console.log(`  Errors:   ${errors}`);
  console.log(`  Total:    ${players.length}`);

  if (errors > 0) {
    process.exit(1);
  }
}

seedPlayers().catch((err) => {
  console.error('Unexpected error:', err);
  process.exit(1);
});
