import * as dotenv from 'dotenv';
dotenv.config({ path: '.env.local' });

import { createClient } from '@supabase/supabase-js';

const supabaseUrl = process.env.NEXT_PUBLIC_SUPABASE_URL!;
const supabaseKey = process.env.SUPABASE_SERVICE_ROLE_KEY!;

if (!supabaseUrl || !supabaseKey) {
  console.error('Missing NEXT_PUBLIC_SUPABASE_URL or SUPABASE_SERVICE_ROLE_KEY in .env.local');
  process.exit(1);
}

const supabase = createClient(supabaseUrl, supabaseKey, {
  auth: { autoRefreshToken: false, persistSession: false },
});

function basePrice(iplPrice: number): number {
  if (iplPrice > 10) return 2;
  if (iplPrice > 5) return 1;
  if (iplPrice > 2) return 0.5;
  return 0.2;
}

interface PlayerSeed {
  name: string;
  team: string;
  role: 'Batter' | 'Bowler' | 'All-Rounder' | 'Wicketkeeper';
  nationality: 'Indian' | 'Overseas';
  country: string;
  batting_style: string;
  bowling_style: string;
  ipl_price: number;
  base_price: number;
  rating: number;
  photo_url: string | null;
  career_stats: {
    matches: number;
    runs: number;
    wickets: number;
    batting_avg: number;
    batting_sr: number;
    bowling_avg: number;
    economy: number;
    highest_score: number;
    best_bowling: string;
    fifties: number;
    hundreds: number;
  };
  season: string;
}

function p(
  name: string,
  team: string,
  role: PlayerSeed['role'],
  nationality: PlayerSeed['nationality'],
  country: string,
  batting_style: string,
  bowling_style: string,
  ipl_price: number,
  rating: number,
  career_stats: PlayerSeed['career_stats'],
  photo_url: string | null = null
): PlayerSeed {
  return {
    name,
    team,
    role,
    nationality,
    country,
    batting_style,
    bowling_style,
    ipl_price,
    base_price: basePrice(ipl_price),
    rating,
    photo_url,
    career_stats,
    season: 'IPL 2026',
  };
}

